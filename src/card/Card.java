package card;

public class Card {
	String id;
	double money;
	String bank;
	
	public void setId(String i) {
		id = i ;
	}
	public void setMoney(double m) {
		money = m ;
	}
	public void setBank(String b) {
		bank = b ;
	}
	public String getId() {
		return id;
	}
	public double getMoney() {
		return money;
	}
	public String getBank() {
		return bank;
	}

}
